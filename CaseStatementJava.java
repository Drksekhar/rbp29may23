package com.bridgelabz;

public class CaseStatementJava {
    public static void main(String[] args) {
        int num = 2;
        switch (num) {
        case 1:
            System.out.println("one");
                    break;
        case 2:
            System.out.println("two");
                    break;
        case 3:
            System.out.println("Three");
            break;
        default:
            System.out.println("Enter Valid Number");
    }
}
}
